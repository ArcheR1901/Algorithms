def subtraction(a, b):
    """
    Subtraction of two numbers
    https://en.wikipedia.org/wiki/Subtraction

    >>> subtraction(7, 2)
    5

    >>> subtraction(75, 5)
    70

    >>> subtraction(4, 7)
    -3

    """
    return a - b


if __name__ == "__main__":
    a = 5
    b = 6
    print(subtraction(a, b))
