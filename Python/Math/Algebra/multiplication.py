def multiplication(a, b):
    """
    Multiplication of two numbers
    https://en.wikipedia.org/wiki/Multiplication
    
    >>> multiplication(4, 2)
    8
    
    >>> multiplication(7, -3)
    -21
    
    >>> multiplication(22, 7)
    154
    
    """
    return a * b

if __name__ == "__main__":
    a = 5
    b = 6
    print(multiplication(a, b))
